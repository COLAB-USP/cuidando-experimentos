              <p>O Cuidando do meu bairro Creches apresenta a situação da rede de creches da Prefeitura Municipal de São Paulo – PMSP, no ano de 2014. A partir dos mapas disponíveis é possível identificar a localização das creches existentes, tanto da rede direta quanto conveniada, e algumas de suas características, informadas no Censo Escolar do Ministério da Educação. Além disso, é possível verificar a situação atual da fila por creches nos diferentes distritos da Cidade, conforme os dados divulgados pela PMSP, por meio do cadastro Escola On-line.  </p>
              <p>De acordo com as informações divulgadas pela Prefeitura, a rede municipal é composta, atualmente, por 1665 Centros de Educação Infantil - CEI, sendo 315 diretos e 1350 conveniados (359 indiretos e 991 creches particulares conveniadas), ou seja, o atendimento conveniado representa 81% do total dos CEI existentes. São mais de 226 mil crianças atendidas na faixa etária de 0 a 3 anos e 11 meses. O município de São Paulo possui a maior rede de creches do país e atende cerca de 50% do total de crianças nesta faixa de idade, indicando convergência à meta do Plano Nacional de Educação.</p>
              <p>Apesar do grande do número de matrículas oferecidas em creche, a fila de demanda por creche na capital atingiu, em setembro de 2014, a marca de mais de 160 mil crianças cadastradas. A distribuição da fila entre os 96 distritos é desigual e observa-se, por exemplo, uma concentração maior de cadastros em alguns distritos na Zona Sul da Cidade. </p>
              <p>A partir do Mapa das Creches o Cuidando do meu bairro pretende contribuir com a divulgação de informações para o público em geral sobre a situação do atendimento em creche no município de São Paulo, assim como colaborar com novas formas de apresentação dos dados existentes, tornando-os mais acessíveis e fomentando o controle social. </p>
